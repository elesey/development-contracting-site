export const FloatingNavDock = ({
  items = defaultItems,
  className,
}: {
  items?: NavItem[];
  className?: string;
}) => {
  return (
    <div
      className={cn("fixed bottom-8 left-1/2 z-50 -translate-x-1/2", className)}
    >
      <FloatingDockDesktop items={items} />
      <FloatingDockMobile items={items} />
    </div>
  );
};

const FloatingDockMobile = ({ items }: { items: NavItem[] }) => {
  const [open, setOpen] = useState(false);
  return (
    <div className="relative block md:hidden">
      <AnimatePresence>
        {open && (
          <motion.div
            layoutId="nav-mobile"
            className="absolute inset-x-0 bottom-full mb-2 flex flex-col gap-2"
          >
            {items.map((item, idx) => (
              <motion.div
                key={item.title}
                initial={{ opacity: 0, y: 10 }}
                animate={{
                  opacity: 1,
                  y: 0,
                }}
                exit={{
                  opacity: 0,
                  y: 10,
                  transition: {
                    delay: idx * 0.05,
                  },
                }}
                transition={{ delay: (items.length - 1 - idx) * 0.05 }}
              >
                <a
                  href={item.href}
                  onClick={() => setOpen(false)}
                  className="bg-bone border-line flex h-12 w-12 items-center justify-center rounded-full border shadow-lg"
                >
                  <div className="text-ink h-5 w-5">{item.icon}</div>
                </a>
              </motion.div>
            ))}
          </motion.div>
        )}
      </AnimatePresence>
      <button
        onClick={() => setOpen(!open)}
        className="bg-ink border-graphite flex h-14 w-14 items-center justify-center rounded-full border shadow-xl"
      >
        <div className="text-chalk h-6 w-6">
          <MenuIcon />
        </div>
      </button>
    </div>
  );
};

const FloatingDockDesktop = ({ items }: { items: NavItem[] }) => {
  let mouseX = useMotionValue(Infinity);
  return (
    <motion.div
      onMouseMove={(e) => mouseX.set(e.pageX)}
      onMouseLeave={() => mouseX.set(Infinity)}
      className="bg-bone/95 border-line mx-auto hidden h-16 items-end gap-3 rounded-full border px-4 pb-2 shadow-xl backdrop-blur-md md:flex"
    >
      {items.map((item) => (
        <IconContainer mouseX={mouseX} key={item.title} {...item} />
      ))}
    </motion.div>
  );
};

function IconContainer({
  mouseX,
  title,
  icon,
  href,
}: {
  mouseX: MotionValue;
  title: string;
  icon: React.ReactNode;
  href: string;
}) {
  let ref = useRef<HTMLDivElement>(null);

  let distance = useTransform(mouseX, (val) => {
    let bounds = ref.current?.getBoundingClientRect() ?? { x: 0, width: 0 };
    return val - bounds.x - bounds.width / 2;
  });

  let widthTransform = useTransform(distance, [-150, 0, 150], [44, 72, 44]);
  let heightTransform = useTransform(distance, [-150, 0, 150], [44, 72, 44]);

  let widthTransformIcon = useTransform(distance, [-150, 0, 150], [20, 32, 20]);
  let heightTransformIcon = useTransform(
    distance,
    [-150, 0, 150],
    [20, 32, 20],
  );

  let width = useSpring(widthTransform, {
    mass: 0.1,
    stiffness: 150,
    damping: 12,
  });
  let height = useSpring(heightTransform, {
    mass: 0.1,
    stiffness: 150,
    damping: 12,
  });

  let widthIcon = useSpring(widthTransformIcon, {
    mass: 0.1,
    stiffness: 150,
    damping: 12,
  });
  let heightIcon = useSpring(heightTransformIcon, {
    mass: 0.1,
    stiffness: 150,
    damping: 12,
  });

  const [hovered, setHovered] = useState(false);

  return (
    <a href={href}>
      <motion.div
        ref={ref}
        style={{ width, height }}
        onMouseEnter={() => setHovered(true)}
        onMouseLeave={() => setHovered(false)}
        className="bg-chalk border-line hover:border-cedar relative flex aspect-square items-center justify-center rounded-full border transition-colors"
      >
        <AnimatePresence>
          {hovered && (
            <motion.div
              initial={{ opacity: 0, y: 10, x: "-50%" }}
              animate={{ opacity: 1, y: 0, x: "-50%" }}
              exit={{ opacity: 0, y: 2, x: "-50%" }}
              className="bg-ink text-chalk absolute -top-10 left-1/2 w-fit rounded-md px-3 py-1.5 text-xs font-medium tracking-wider whitespace-pre uppercase"
            >
              {title}
            </motion.div>
          )}
        </AnimatePresence>
        <motion.div
          style={{ width: widthIcon, height: heightIcon }}
          className="text-ink flex items-center justify-center"
        >
          {icon}
        </motion.div>
      </motion.div>
    </a>
  );
}
